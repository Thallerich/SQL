DECLARE @VsaList TABLE (
  VsaID int
);

INSERT INTO @VsaList
SELECT DISTINCT Vsa.ID
FROM Vsa
JOIN Kunden ON Vsa.KundenID = Kunden.ID
WHERE Kunden.KdNr IN (110112, 110664, 270338, 272917, 270576, 218032, 272739, 10001763, 120024, 120008, 120090, 120045, 10001764, 219014, 215019, 10001762, 10001760, 10001761, 272953, 120159, 120051, 110006, 111393, 111529, 10004298, 10001621, 170013, 271624, 271636, 271576, 271615, 10000467, 271494, 271582, 10000598, 10003468, 202626, 279535, 272033, 215501, 215511, 215503, 215502, 219630, 215500, 215514)
  AND Vsa.NichtImmerLS = 0
  AND Vsa.Status = N'A'
  AND Kunden.Status = N'A';

UPDATE Vsa SET NichtImmerLS = 1
WHERE Vsa.ID IN (
  SELECT VsaID
  FROM @VsaList
);

SELECT Kunden.KdNr, Kunden.Suchcode AS Kunde, Vsa.VsaNr, Vsa.Bez AS VsaBezeichnung
FROM Vsa
JOIN Kunden ON Vsa.KundenID = Kunden.ID
WHERE Vsa.ID IN (
  SELECT VsaID
  FROM @VsaList
)
ORDER BY Kunden.KdNr, Vsa.VsaNr;